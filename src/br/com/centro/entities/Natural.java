package br.com.centro.entities;

import java.util.Date;

public class Natural extends Person{
    
    private String cpfNatural;
    private String rgNatural;
    private Date dateNatural;
    private String emailNatural;
    private String phoneNatural;
    private String cellularNatural;
    private String registerNatural;
    private String typeNatural;

    public String getCpfNatural() {
        return cpfNatural;
    }

    public void setCpfNatural(String cpfNatural) {
        this.cpfNatural = cpfNatural;
    }

    public String getRgNatural() {
        return rgNatural;
    }

    public void setRgNatural(String rgNatural) {
        this.rgNatural = rgNatural;
    }

    public Date getDateNatural() {
        return dateNatural;
    }

    public void setDateNatural(Date dateNatural) {
        this.dateNatural = dateNatural;
    }

    public String getEmailNatural() {
        return emailNatural;
    }

    public void setEmailNatural(String emailNatural) {
        this.emailNatural = emailNatural;
    }

    public String getPhoneNatural() {
        return phoneNatural;
    }

    public void setPhoneNatural(String phoneNatural) {
        this.phoneNatural = phoneNatural;
    }

    public String getCellularNatural() {
        return cellularNatural;
    }

    public void setCellularNatural(String cellularNatural) {
        this.cellularNatural = cellularNatural;
    }

    public String getRegisterNatural() {
        return registerNatural;
    }

    public void setRegisterNatural(String registerNatural) {
        this.registerNatural = registerNatural;
    }

    public String getTypeNatural() {
        return typeNatural;
    }

    public void setTypeNatural(String typeNatural) {
        this.typeNatural = typeNatural;
    }
    
}
