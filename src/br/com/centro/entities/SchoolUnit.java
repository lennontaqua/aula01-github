package br.com.centro.entities;

public class SchoolUnit {
    
    private Long codeSchoolUnit;
    private String nameSchoolUnit;
    private String opSchoolUnit;
    private String phoneSchoolUnit;
    private String homePageSchoolUnit;
    private String emailSchoolUnit;

    public Long getCodeSchoolUnit() {
        return codeSchoolUnit;
    }

    public void setCodeSchoolUnit(Long codeSchoolUnit) {
        this.codeSchoolUnit = codeSchoolUnit;
    }

    public String getNameSchoolUnit() {
        return nameSchoolUnit;
    }

    public void setNameSchoolUnit(String nameSchoolUnit) {
        this.nameSchoolUnit = nameSchoolUnit;
    }

    public String getOpSchoolUnit() {
        return opSchoolUnit;
    }

    public void setOpSchoolUnit(String opSchoolUnit) {
        this.opSchoolUnit = opSchoolUnit;
    }

    public String getPhoneSchoolUnit() {
        return phoneSchoolUnit;
    }

    public void setPhoneSchoolUnit(String phoneSchoolUnit) {
        this.phoneSchoolUnit = phoneSchoolUnit;
    }

    public String getHomePageSchoolUnit() {
        return homePageSchoolUnit;
    }

    public void setHomePageSchoolUnit(String homePageSchoolUnit) {
        this.homePageSchoolUnit = homePageSchoolUnit;
    }

    public String getEmailSchoolUnit() {
        return emailSchoolUnit;
    }

    public void setEmailSchoolUnit(String emailSchoolUnit) {
        this.emailSchoolUnit = emailSchoolUnit;
    }
}
